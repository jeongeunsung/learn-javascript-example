// 예) setHexColor(255, 255, 255) // 결과값: '#ffffff'

function setHexColot(hexValue, hexValue, hexValue) {
  // const hexValues = hexValue + hexValue + hexValue
  return parseInt(hexValue + hexValue + hexValue, 10)
}

